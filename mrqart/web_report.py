# Why not always have jinja2?
try:
    from jinja2 import Environment, FileSystemLoader, select_autoescape, Template
    _HAS_JINJA = True
except Exception:
    _HAS_JINJA = False

def _ensure_parent(p: Path) -> None:
    p.parent.mkdir(parents=True, exist_ok=True)

def append_entries(entries: Iterable[Dict[str, Any]], log_path: Path) -> None:
    """Append entries as JSONL to log_path."""
    _ensure_parent(log_path)
    with log_path.open("a", encoding="utf-8") as f:
        for e in entries:
            f.write(json.dumps(e, ensure_ascii=False) + "\n")

def _read_jsonl(path: Path) -> List[Dict[str, Any]]:
    if not path.exists():
        return []
    rows: List[Dict[str, Any]] = []
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            s = line.strip()
            if not s:
                continue
            try:
                rows.append(json.loads(s))
            except Exception:
                # skip bad line
                pass
    return rows

def _sort_rows(rows: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    rows.sort(key=lambda r: r.get("acq_ts_sort") or r.get("script_ts") or "", reverse=True)
    return rows

