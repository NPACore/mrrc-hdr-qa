def _badge_bool(b) -> str:
    if b is True:  return '<span class="chip ok">True</span>'
    if b is False: return '<span class="chip bad">False</span>'
    return '<span class="chip na">N/A</span>'

def _status_badge(s: str) -> str:
    color = {"OK":"#10b981","NONCONFORM":"#ef4444","NO_TEMPLATE":"#f59e0b"}.get(s, "#6b7280")
    return f'<span class="badge" style="background:{color};">{html.escape(s or "")}</span>'

def _fmt_errs(errs) -> str:
    if not errs: return ""
    if isinstance(errs, str): return html.escape(errs)
    return "<ul>" + "".join(f"<li>{html.escape(str(e))}</li>" for e in errs) + "</ul>"

def _render_fallback(rows: List[Dict[str, Any]], html_out: Path, title: str) -> None:
    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    trs = []
    for r in rows:
        trs.append(
            f"""<tr data-status="{html.escape(str(r.get('status','')))}">
  <td class="nowrap">{html.escape(str(r.get('script_ts','')))}</td>
  <td class="nowrap">{html.escape(str(r.get('acq_ts','')))}</td>
  <td>{html.escape(str(r.get('project','')))}</td>
  <td>{html.escape(str(r.get('sequence','')))}</td>
  <td>{html.escape(str(r.get('subid','')))}</td>
  <td class="num">{html.escape(str(r.get('series','')))}</td>
  <td>{html.escape(str(r.get('station','')))}</td>
  <td class="center">{_badge_bool(r.get('conforms'))}</td>
  <td class="num">{html.escape(str(r.get('error_count','')))}</td>
  <td>{_fmt_errs(r.get('errors'))}</td>
  <td>{_status_badge(str(r.get('status','')))}</td>
</tr>"""
        )
    out = _FALLBACK_BODY.format(
        title=html.escape(title),
        css=_FALLBACK_CSS,
        now=html.escape(now),
        N=len(rows),
        rows_html="\n".join(trs),
    )
    _ensure_parent(html_out)
    html_out.write_text(out, encoding="utf-8")

# -------------------------
# Public: render_html
# -------------------------
def render_html(
    log_path: Path,
    html_out: Path,
    title: str = "MRQART QA — Feed",
    max_rows: int = 2000,
    template_path: Optional[Path] = None,
) -> None:
    """
    Render a static HTML from JSONL log.
    If Jinja2 is available and a template is found, render with Jinja.
    Otherwise fall back to the built-in renderer (same look as before).
    """
    rows = _read_jsonl(log_path)
    rows = _sort_rows(rows)
    if max_rows and len(rows) > max_rows:
        rows = rows[:max_rows]

    if _HAS_JINJA:
        # Resolve template: env var > passed path > default in static/
        tpl_env = os.environ.get("MRQART_WEB_TEMPLATE")
        tpl_file = Path(tpl_env) if tpl_env else None
        if not tpl_file and template_path:
            tpl_file = Path(template_path)
        if not tpl_file:
            # Default location: <repo>/static/mrqart_report.html.j2
            tpl_file = Path(__file__).resolve().parent.parent / "static" / "mrqart_report.html.j2"

        if tpl_file.exists():
            # Jinja environment pointing at template directory
            loader = FileSystemLoader(str(tpl_file.parent))
            env = Environment(loader=loader, autoescape=select_autoescape(["html", "xml"]))
            tmpl: Template = env.get_template(tpl_file.name)

            ctx = {
                "title": title,
                "rows": rows,
                "updated": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
                "count": len(rows),
            }
            _ensure_parent(html_out)
            html_out.write_text(tmpl.render(**ctx), encoding="utf-8")
            return
        # else: fall through to fallback if template missing

    # Fallback path: no Jinja or template not found
    _render_fallback(rows, html_out, title)

